int1 = 10
float1 = 10.5

print(int1)
print(float1)

# type(name) 判断数据类型
print(type(int1)) #<class 'int'>
print(type(float1)) #<class 'float'>

b1 = True
b2 = False
print(b1)
print(b2)

print(type(b1)) #<class 'bool'>
print(type(b2)) #<class 'bool'>

str1 = 'KK 做自己'
print(str1)
print(type(str1)) #<class 'str'>

# 容器
list1 = [10, 32, 46, 89]
print(list1)
print(type(list1)) #<class 'list'>

tuple1=(100, 200, 89)
print(tuple1)
print(type(tuple1)) #<class 'tuple'>

set1 = { 101, 6, 78 }
print(set1)
print(type(set1)) #<class 'set'>

dict1 = { 'name': 'KK', 'age': 18 }
print(dict1)
print(type(dict1)) #<class 'dict'>


